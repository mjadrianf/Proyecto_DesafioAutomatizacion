package CodigoEjemplo;

public class StringClassEjemplo {
    public static void main(String[] args) {
        byte b1 = 21;
        short enteroCorto = 300;
        int entero = 25;
        long longNumber = 1000000;
        double balance = 112.24;
        float puntoflotante = 300.3f;
        boolean bool = true;
        char caracter = 'a';
        String miNombre = "MariaJose";

        System.out.println("este es mi byte : " + b1);
        System.out.println("este es mi int : " + entero);
        System.out.println("este es mi short  : " + enteroCorto);
        System.out.println("este es mi long : " + longNumber);
        System.out.println("este es mi double : " + balance);
        System.out.println("este es mi puntoflotante : " + puntoflotante);
        System.out.println("este es mi bool : " + bool);
        System.out.println("este es mi caracter : " + caracter);
        System.out.println("este es mi string : " + miNombre);
    }
}
